package cuny.blender.englishie.algorithm.clustering;

import java.util.ArrayList;

public class ClusterItem {
	int id;
	String content;
	String classId;
	boolean used;
	ArrayList<Integer> seedIndexes = new ArrayList<Integer> ();
	
	public ClusterItem(int id, String content){
		this.id = id;
		this.content = content;
	}
	
	public String getContent(){
		return this.content;
	}
	
	public void setClassId(String classId){
		this.classId = classId;
	}
	
	public String getClassId(){
		return this.classId;
	}
	
	public ArrayList<Integer> getSeedIndexes (){
		return this.seedIndexes;
	}
	
}
